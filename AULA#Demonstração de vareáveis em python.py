# Demonstração de variáveis em python

# Não precisa informar qual é a variavel, python ja sabe.
idade = 18
nome = 'Philippe Augusto'
numero = 5.89

print(idade)
print(nome)
print(numero)

# Como é usado para o print ler as variaveis
print(f"Meu nome é {nome} e tenho {idade} anos")

# exibe o tipo da variável
print(type(nome))
print(type(idade))
print(type(numero))