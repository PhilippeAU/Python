# Cast é uma técnica de conversão de tipos
X = "55"
Y = 10

# Apresenta  tipo da variável x
print(type(X))


print(X + "10")

# Converte x em inteiro para soma com 10
print(int(X) + 10)

# converte o testo 896 para o número inteiro 986 e soma 10
print(int("896") + 10)

# Converte  y, que é um inteiro 10, para o TEXTO 10 e contatena 
print(X + str(Y))

